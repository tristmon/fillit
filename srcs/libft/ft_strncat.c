#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t nb)
{
	unsigned long	i;
	unsigned long	j;

	i = ft_strlen(dest);
	j = 0;
	nb = nb + i;
	while (i < nb && src[j])
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
